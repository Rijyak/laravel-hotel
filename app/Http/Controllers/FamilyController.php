    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'name'=> 'required',
            'address'=> 'required',
            'phone'=> 'required',
            'email'=> 'required',
            'checkin'=> 'required',
            'checkout'=> 'required',
        ]);

        $frooms = new Family;

        $frooms -> name = $request->input('name');
        $frooms -> address = $request->input('address');
        $frooms -> phone = $request->input('phone');
        $frooms -> email = $request->input('email');
        $frooms -> checkin = $request->input('checkin');
        $frooms -> checkout = $request->input('checkout');

        $frooms -> save();

        return redirect('/rooms')-> with('success', 'Data sent');
    }

